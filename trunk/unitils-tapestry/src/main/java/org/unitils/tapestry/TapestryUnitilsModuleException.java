package org.unitils.tapestry;

public class TapestryUnitilsModuleException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public TapestryUnitilsModuleException() {
		super();
	}

	public TapestryUnitilsModuleException(String message, Throwable cause) {
		super(message, cause);
	}

	public TapestryUnitilsModuleException(String message) {
		super(message);
	}

	public TapestryUnitilsModuleException(Throwable cause) {
		super(cause);
	}
	
}
